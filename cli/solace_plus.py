# ── Paths & env ──────────────────────────────────────────────────────────────

PROJECT_ROOT = Path(__file__).parent.parent.resolve()
REGISTRY_PATH = PROJECT_ROOT / "config" / "topic-mv-registry.yaml"

load_dotenv(PROJECT_ROOT / ".env")


# ── Config helpers ────────────────────────────────────────────────────────────

def _expand_env(text: str) -> str:
    """Expand ${VAR:-default} patterns using os.environ."""
    def _replace(m: re.Match) -> str:
        var, _, default = m.group(1).partition(":-")
        return os.environ.get(var, default)
    return re.sub(r"\$\{([^}]+)\}", _replace, text)


def load_registry() -> dict:
    """Load and return topic-mv-registry.yaml, expanding env-var placeholders."""
    if not REGISTRY_PATH.exists():
        click.echo(
            f"ERROR: Registry not found at {REGISTRY_PATH}\n"
            "Run 'python3 generate_mvs.py --skip-ep' to generate it (no EP token needed).\n"
            "Or run 'python3 generate_mvs.py' if SOLACE_CLOUD_TOKEN is set.",
            err=True,
        )
        sys.exit(1)
    text = _expand_env(REGISTRY_PATH.read_text())
    return yaml.safe_load(text)


_registry: Optional[dict] = None
_rw_conn = None


def get_registry() -> dict:
    global _registry
    if _registry is None:
        _registry = load_registry()
    return _registry


def get_rw_conn():
    global _rw_conn
    if _rw_conn is None or _rw_conn.closed:
        rw = get_registry()["risingwave"]
        _rw_conn = psycopg2.connect(
            host=rw["host"],
            port=int(rw.get("port", 4566)),
            dbname=rw.get("database", "dev"),
            user=rw.get("user", "root"),
            password=rw.get("password", ""),
        )
        _rw_conn.autocommit = True
    return _rw_conn


def get_solace_config() -> dict:
    return get_registry()["solace"]


# ── Pattern matching ──────────────────────────────────────────────────────────

def _pat_to_regex(pattern: str) -> re.Pattern:
    """Convert a Solace topic pattern (with * and >) to a regex."""
    parts = re.escape(pattern).replace(r"\*", "[^/]+").replace(r"\>", ".+")
    return re.compile(f"^{parts}$")


def find_entry(pattern: str) -> Optional[dict]:
    """Return the best registry entry for *pattern* (exact match preferred)."""
    mappings = get_registry().get("mappings", [])

    # 1. Exact match
    for m in mappings:
        if m["pattern"] == pattern:
            return m

    # 2. Registry wildcard pattern covers the query pattern
    for m in mappings:
        if _pat_to_regex(m["pattern"]).match(pattern):
            return m

    # 3. Query pattern (wildcard) covers the registry pattern
    if "*" in pattern or ">" in pattern:
        query_re = _pat_to_regex(pattern)
        for m in mappings:
            if query_re.match(m["pattern"]):
                return m

    return None


# ── Duration helpers ──────────────────────────────────────────────────────────

_DURATION_RE = re.compile(r"^(\d+)(d|h|hr|min|m|s)$", re.IGNORECASE)
_UNIT_MAP = {
    "d": "days", "h": "hours", "hr": "hours",
    "min": "minutes", "m": "minutes", "s": "seconds",
}


def duration_to_pg(s: str) -> str:
    """Convert a duration string (1h, 30min, 2d) to a PostgreSQL interval string."""
    m = _DURATION_RE.match(s.strip())
    if not m:
        raise click.BadParameter(
            f"Invalid duration '{s}' — use e.g. 1h, 30min, 2d, 10s"
        )
    n, unit = m.group(1), _UNIT_MAP[m.group(2).lower()]
    return f"{n} {unit}"


# ── Output helpers ────────────────────────────────────────────────────────────

def _serialize(v):
    """Make a value JSON-serializable."""
    if hasattr(v, "isoformat"):
        return v.isoformat()
    return v


def format_and_print(rows_raw: list, fmt: str) -> None:
    """Print a list of RealDictRow objects in the requested format."""
    if not rows_raw:
        click.echo("(no rows)")
        return
    headers = list(rows_raw[0].keys())
    if fmt == "json":
        for r in rows_raw:
            click.echo(json.dumps({k: _serialize(v) for k, v in r.items()}))
    elif fmt == "csv":
        click.echo(",".join(headers))
        for r in rows_raw:
            click.echo(",".join(str(_serialize(v)) for v in r.values()))
    else:
        rows = [list(r.values()) for r in rows_raw]
        click.echo(tabulate(rows, headers=headers, tablefmt="simple"))


def emit_event(event: dict, fmt: str) -> None:
    """Emit a single event dict (subscribe / alert watch output)."""
    if fmt == "json":
        click.echo(json.dumps({k: _serialize(v) for k, v in event.items()}))
    else:
        ts = _serialize(event.get("event_time", ""))
        source = event.get("source", "")
        topic = event.get("topic", "")
        payload = event.get("payload", {})
        vid = payload.get("vehicle_id", "")
        summary = "  ".join(
            f"{k}={_serialize(v)}"
            for k, v in payload.items()
            if k not in ("solace_topic", "vehicle_id") and v is not None
        )
        click.echo(f"[{source:7}] {ts}  {topic}  vehicle={vid}  {summary}")


# ── Solace SMF subscriber ─────────────────────────────────────────────────────

def start_smf_receiver(pattern: str, msg_queue: queue.Queue) -> threading.Event:
    """
    Connect to Solace via SMF, subscribe to *pattern*, and put received
    messages onto *msg_queue* as dicts:
        {"topic": str, "payload": dict, "source": "live"}

    Returns a threading.Event; set it to stop the receiver thread.
    """
    from solace.messaging.messaging_service import MessagingService
    from solace.messaging.resources.topic_subscription import TopicSubscription
    from solace.messaging.errors.pubsubplus_client_error import PubSubPlusClientError

    cfg = get_solace_config()
    broker_props = {
        "solace.messaging.transport.host":
            cfg["smf_host"],
        "solace.messaging.service.vpn-name":
            cfg["vpn"],
        "solace.messaging.authentication.scheme.basic.username":
            cfg["username"],
        "solace.messaging.authentication.scheme.basic.password":
            cfg["password"],
    }

    stop_event = threading.Event()

    def _run():
        try:
            svc = MessagingService.builder().from_properties(broker_props).build()
            svc.connect()
            receiver = (
                svc.create_direct_message_receiver_builder()
                .with_subscriptions([TopicSubscription.of(pattern)])
                .build()
            )
            receiver.start()
            while not stop_event.is_set():
                msg = receiver.receive_message(timeout=200)  # ms
                if msg is None:
                    continue
                topic = msg.get_destination_name()
                raw = msg.get_payload_as_bytes()
                try:
                    payload = json.loads(raw.decode("utf-8")) if raw else {}
                except (json.JSONDecodeError, UnicodeDecodeError):
                    payload = {"raw": str(raw)}
                msg_queue.put({"topic": topic, "payload": payload, "source": "live"})
            receiver.terminate()
            svc.disconnect()
        except PubSubPlusClientError as exc:
            click.echo(f"[smf] connection error: {exc}", err=True)
        except Exception as exc:  # noqa: BLE001
            click.echo(f"[smf] unexpected error: {exc}", err=True)

    t = threading.Thread(target=_run, daemon=True, name="smf-receiver")
    t.start()
    return stop_event


# ── Dedup / topic reconstruction helpers ─────────────────────────────────────

def _dedup_key(row_or_payload: dict, time_col: str) -> tuple:
    """(vehicle_id, timestamp_string) — unique per event."""
    vid = row_or_payload.get("vehicle_id", "")
    ts = row_or_payload.get(
        time_col,
        row_or_payload.get("recorded_at",
        row_or_payload.get("occurred_at",
        row_or_payload.get("issued_at", ""))),
    )
    return (vid, str(ts))


def _time_col_from_payload(payload: dict) -> str:
    """Guess the timestamp field name from a live message payload."""
    for col in ("recorded_at", "occurred_at", "issued_at"):
        if col in payload:
            return col
    return "recorded_at"


def reconstruct_topic(pattern: str, row: dict) -> str:
    """
    Build a topic string from a registry pattern + MV row data.
    Uses row["solace_topic"] if present; otherwise fills wildcards from row fields.
    """
    if row.get("solace_topic"):
        return row["solace_topic"]
    topic = pattern
    vid = row.get("vehicle_id", "unknown")
    topic = topic.replace("*", vid, 1)
    if "*" in topic:
        filler = (
            row.get("severity")
            or row.get("metric_type")
            or row.get("command_type")
            or "unknown"
        )
        topic = topic.replace("*", filler, 1)
    return topic


