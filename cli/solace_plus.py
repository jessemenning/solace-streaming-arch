#!/usr/bin/env python3
"""
solace+ — Solace CLI extended with historical data via RisingWave.

Uses the topic-mv-registry.yaml (generated by generate_mvs.py) to map
Solace topic patterns to RisingWave materialized views, enabling transparent
historical queries and live subscriptions from a single command.

Commands:
  query     <pattern>              Pure RisingWave query — no live subscription
  subscribe <pattern> [--since]    Replay history then stream live via Solace SMF
  topic     info / lag / list      Registry inspection and data freshness
  alert     list / history / watch Alert shortcuts

Examples:
  python3 cli/solace_plus.py query "fleet/events/*/alerts/high" --since 1h
  python3 cli/solace_plus.py subscribe "fleet/events/*/alerts/high" --since 30min
  python3 cli/solace_plus.py topic lag "fleet/telemetry/*/metrics/speed"
  python3 cli/solace_plus.py alert watch --severity high
"""

