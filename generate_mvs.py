#!/usr/bin/env python3
"""
generate_mvs.py — Generate RisingWave init.sql from Solace Event Portal catalog.

Reads event metadata from the "Fleet Operations" application domain in
Solace Event Portal and writes a complete config/risingwave/init.sql that
mirrors each published event version as a RisingWave materialized view.

Usage:
  python3 generate_mvs.py [--domain "Fleet Operations"] [--dry-run]

Requires:
  SOLACE_CLOUD_TOKEN in .env at project root (or already in environment).
"""

