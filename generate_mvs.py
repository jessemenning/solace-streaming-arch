def _infer_time_column(like_pattern: str) -> str:
    """Infer the timestamp column name from a topic LIKE pattern."""
    if "telemetry" in like_pattern:
        return "recorded_at"
    if "events" in like_pattern:
        return "occurred_at"
    if "commands" in like_pattern:
        return "issued_at"
    return "recorded_at"


def write_registry(path: Path, mappings: list) -> None:
    """Write config/topic-mv-registry.yaml — no PyYAML dependency needed."""
    lines = [
        "# =========================================================================",
        "# topic-mv-registry.yaml — Topic Pattern → RisingWave MV Mapping",
        "# AUTO-GENERATED by generate_mvs.py — DO NOT EDIT BY HAND",
        "# Re-generate: python3 generate_mvs.py",
        "# =========================================================================",
        'version: "1.0"',
        "",
        "risingwave:",
        '  host: "${RW_HOST:-localhost}"',
        '  port: "${RW_PORT:-4566}"',
        "  database: dev",
        "  user: root",
        "",
        "solace:",
        '  smf_host: "${SOLACE_HOST:-tcp://localhost:55555}"',
        '  vpn: "${SOLACE_VPN:-streaming-poc}"',
        '  username: "${SOLACE_USER:-streaming-user}"',
        '  password: "${SOLACE_PASSWORD:-default}"',
        "",
        "mappings:",
    ]
    for m in mappings:
        lines.append(f'  - pattern: "{m["pattern"]}"')
        lines.append(f'    mv: {m["mv"]}')
        if m.get("time_column"):
            lines.append(f'    time_column: {m["time_column"]}')
        if m.get("aggregates"):
            lines.append("    aggregates:")
            for k, v in m["aggregates"].items():
                lines.append(f"      {k}: {v}")
        if m.get("enriched"):
            lines.append(f"    enriched: {m['enriched']}")
        if m.get("spatial"):
            lines.append("    spatial:")
            for k, v in m["spatial"].items():
                lines.append(f"      {k}: {v}")
    lines.append("")
    path.write_text("\n".join(lines))

# ─── .env loader ──────────────────────────────────────────────────────────────

def load_env(env_path: Path) -> None:
    """Parse KEY=value lines from env_path into os.environ (skip if already set)."""
    if not env_path.exists():
        return
    for line in env_path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, _, val = line.partition("=")
        key = key.strip()
        val = val.strip().strip('"').strip("'")
        if key and key not in os.environ:
            os.environ[key] = val


# ─── EP REST helpers ──────────────────────────────────────────────────────────

def ep_get(token: str, path: str, params: dict | None = None) -> dict:
    """GET from EP API; raises on HTTP error."""
    url = EP_BASE + path
    if params:
        url += "?" + urllib.parse.urlencode(params)
    req = urllib.request.Request(url, headers={
        "Authorization": f"Bearer {token}",
        "Accept": "application/json",
    })
    with urllib.request.urlopen(req) as resp:
        return json.loads(resp.read())


def get_all_pages(token: str, path: str, params: dict | None = None) -> list:
    """Paginate through all results for a list endpoint."""
    results = []
    page = 1
    while True:
        p = dict(params or {})
        p["pageSize"] = 100
        p["pageNumber"] = page
        resp = ep_get(token, path, p)
        batch = resp.get("data", [])
        results.extend(batch)
        meta = resp.get("meta", {}).get("pagination", {})
        if page >= meta.get("totalPages", 1):
            break
        page += 1
    return results


def get_domain_id(token: str, domain_name: str) -> str:
    resp = ep_get(token, "/applicationDomains", {"name": domain_name, "pageSize": 5})
    data = resp.get("data", [])
    if not data:
        raise SystemExit(f"ERROR: No application domain found named '{domain_name}'")
    return data[0]["id"]


# ─── Conversion helpers ───────────────────────────────────────────────────────

def address_levels_to_like(levels: list[dict]) -> str:
    """Convert EP deliveryDescriptor address levels to a SQL LIKE pattern."""
    parts = []
    for lvl in levels:
        if lvl.get("addressLevelType") == "literal":
            parts.append(lvl["name"])
        else:
            parts.append("%")
    return "/".join(parts)


def schema_columns(schema_content_str: str) -> list[str]:
    """
    Return the subset of SOURCE_COLUMNS that appear as top-level properties
    in the JSON Schema, preserving the canonical column order.
    """
    try:
        schema = json.loads(schema_content_str)
    except (json.JSONDecodeError, TypeError):
        return ["vehicle_id", "solace_topic"]
    props = set(schema.get("properties", {}).keys())
    return [c for c in SOURCE_COLUMNS_ORDERED if c in props & SOURCE_COLUMNS]


def mv_name(event_name: str) -> str:
    """Convert event name to a valid SQL identifier (hyphens/spaces → underscores)."""
    return re.sub(r"[^a-z0-9_]", "_", event_name.lower())


# ─── Static SQL blocks ────────────────────────────────────────────────────────

STATIC_HEADER = """\
-- =============================================================================
-- RisingWave Streaming SQL — Fleet Monitoring POC
-- AUTO-GENERATED by generate_mvs.py — DO NOT EDIT BY HAND
-- Re-generate: python3 generate_mvs.py
-- =============================================================================
-- Architecture note:
--   The Solace Source Connector routes ALL queue messages to one Redpanda
--   topic (fleet-events).  Every message embeds its original Solace topic in
--   the `solace_topic` field.  RisingWave uses that field to filter streams —
--   replacing Solace wildcard subscriptions with SQL WHERE clauses.
--
--   Routing MVs are generated from the Solace Event Portal event catalog.
--   Each event version in the "Fleet Operations" domain becomes one MV.
--
--   fleet-events (Redpanda)
--     └─ fleet_all_raw          (source — everything)
--          ├─ fleet_telemetry_raw   (static — WHERE solace_topic LIKE 'fleet/telemetry/%')
--          ├─ fleet_events_raw      (static — WHERE solace_topic LIKE 'fleet/events/%')
--          ├─ fleet_commands_raw    (static — WHERE solace_topic LIKE 'fleet/commands/%')
--          └─ <event-name>          (EP-generated — one per event version)
-- =============================================================================""".rstrip()

STATIC_SOURCE = """\
-- ── Unified source — one Redpanda topic, all message types ───────────────────
-- All fields from telemetry, alert, and command payloads declared here.
-- Fields absent in a given message arrive as NULL (RisingWave JSON is lenient).
CREATE SOURCE fleet_all_raw (
    vehicle_id    VARCHAR,

    -- Telemetry fields (null in alert / command messages)
    metric_type   VARCHAR,
    value         DOUBLE PRECISION,
    unit          VARCHAR,
    latitude      DOUBLE PRECISION,
    longitude     DOUBLE PRECISION,
    recorded_at   TIMESTAMPTZ,

    -- Alert fields (null in telemetry / command messages)
    event_type    VARCHAR,
    severity      VARCHAR,
    description   VARCHAR,
    payload       JSONB,
    occurred_at   TIMESTAMPTZ,

    -- Command fields (null in telemetry / alert messages)
    command_type  VARCHAR,
    parameters    JSONB,
    issued_at     TIMESTAMPTZ,
    issued_by     VARCHAR,

    -- Routing key — the original Solace topic address embedded in every message
    solace_topic  VARCHAR
) WITH (
    connector                    = 'kafka',
    topic                        = 'fleet-events',
    properties.bootstrap.server  = 'redpanda:9092',
    scan.startup.mode            = 'earliest'
) FORMAT PLAIN ENCODE JSON;""".rstrip()

STATIC_BRANCH_MVS = """\
-- ── Branch routing views — broad-pattern subscriptions ───────────────────────
-- Referenced by the analytics MVs below.  Keep these even when per-event
-- MVs exist — analytics queries join/filter across entire message families.

CREATE MATERIALIZED VIEW fleet_telemetry_raw AS
SELECT vehicle_id, metric_type, value, unit,
       latitude, longitude, recorded_at, solace_topic
FROM   fleet_all_raw
WHERE  solace_topic LIKE 'fleet/telemetry/%';

CREATE MATERIALIZED VIEW fleet_events_raw AS
SELECT vehicle_id, event_type, severity, description,
       payload, occurred_at, solace_topic
FROM   fleet_all_raw
WHERE  solace_topic LIKE 'fleet/events/%';

CREATE MATERIALIZED VIEW fleet_commands_raw AS
SELECT vehicle_id, command_type, parameters,
       issued_at, issued_by, solace_topic
FROM   fleet_all_raw
WHERE  solace_topic LIKE 'fleet/commands/%';""".rstrip()

STATIC_ANALYTICS_MVS = """\
-- =============================================================================
-- ANALYTICS MATERIALIZED VIEWS — "Virtual Topics"
-- =============================================================================

-- ─── 1. High-severity alerts ─────────────────────────────────────────────────
CREATE MATERIALIZED VIEW high_severity_alerts AS
SELECT vehicle_id, event_type, description, payload, occurred_at, solace_topic
FROM   fleet_events_raw
WHERE  severity = 'high';

-- ─── 2. Alert severity tiers ─────────────────────────────────────────────────
CREATE MATERIALIZED VIEW medium_severity_alerts AS
SELECT vehicle_id, event_type, description, payload, occurred_at
FROM   fleet_events_raw
WHERE  severity = 'medium';

CREATE MATERIALIZED VIEW low_severity_alerts AS
SELECT vehicle_id, event_type, description, payload, occurred_at
FROM   fleet_events_raw
WHERE  severity = 'low';

-- ─── 3. Real-time speed readings ─────────────────────────────────────────────
CREATE MATERIALIZED VIEW vehicle_speeds AS
SELECT vehicle_id, value AS speed_mph, latitude, longitude, recorded_at, solace_topic
FROM   fleet_telemetry_raw
WHERE  metric_type = 'speed';

-- ─── 4. 5-minute tumbling window — speed averages per vehicle ────────────────
CREATE MATERIALIZED VIEW vehicle_speed_5min_avg AS
SELECT
    vehicle_id,
    window_start,
    window_end,
    AVG(value)  AS avg_speed_mph,
    MAX(value)  AS max_speed_mph,
    MIN(value)  AS min_speed_mph,
    COUNT(*)    AS sample_count
FROM TUMBLE(fleet_all_raw, recorded_at, INTERVAL '5 MINUTES')
WHERE metric_type = 'speed'
  AND recorded_at IS NOT NULL
GROUP BY vehicle_id, window_start, window_end;

-- ─── 5. Fleet-wide alert summary — rolling 1-hour window ─────────────────────
CREATE MATERIALIZED VIEW fleet_alert_summary AS
SELECT
    severity,
    COUNT(*)                   AS alert_count,
    COUNT(DISTINCT vehicle_id) AS affected_vehicles
FROM fleet_events_raw
WHERE occurred_at > NOW() AT TIME ZONE 'UTC' - INTERVAL '1 HOUR'
GROUP BY severity;

-- ─── 6. Per-vehicle fuel levels ──────────────────────────────────────────────
CREATE MATERIALIZED VIEW vehicle_fuel_levels AS
SELECT vehicle_id, value AS fuel_pct, recorded_at
FROM   fleet_telemetry_raw
WHERE  metric_type = 'fuel_level';

-- ─── 7. High engine temperature alerts ───────────────────────────────────────
CREATE MATERIALIZED VIEW high_engine_temp_vehicles AS
SELECT vehicle_id, value AS engine_temp_f, recorded_at
FROM   fleet_telemetry_raw
WHERE  metric_type = 'engine_temp'
  AND  value > 220;

-- ─── 8. Alerts with correlated telemetry (stream-stream join) ────────────────
CREATE MATERIALIZED VIEW alerts_with_context AS
SELECT
    e.vehicle_id,
    e.event_type,
    e.severity,
    e.description,
    e.occurred_at                    AS alert_time,
    t.metric_type,
    t.value                          AS metric_value,
    t.unit,
    t.recorded_at                    AS metric_time,
    e.occurred_at - t.recorded_at    AS time_before_alert
FROM fleet_events_raw    e
JOIN fleet_telemetry_raw t
    ON  e.vehicle_id = t.vehicle_id
    AND t.recorded_at BETWEEN e.occurred_at - INTERVAL '2 MINUTES'
                          AND e.occurred_at;

-- ─── 9. Geofence — vehicles in Boston metro bounding box ─────────────────────
CREATE MATERIALIZED VIEW vehicles_in_region_boston AS
SELECT vehicle_id, latitude, longitude, value AS speed_mph, recorded_at
FROM   fleet_telemetry_raw
WHERE  metric_type = 'speed'
  AND  latitude  BETWEEN 42.2 AND 42.5
  AND  longitude BETWEEN -71.2 AND -70.9;

-- ─── 10. Per-vehicle latest position ─────────────────────────────────────────
CREATE MATERIALIZED VIEW vehicle_last_known_position AS
SELECT vehicle_id, latitude, longitude, value AS speed_mph, recorded_at
FROM   fleet_telemetry_raw
WHERE  metric_type = 'speed';

-- ─── 11. 1-hour event count per vehicle ──────────────────────────────────────
CREATE MATERIALIZED VIEW vehicle_event_counts_1h AS
SELECT
    vehicle_id,
    severity,
    COUNT(*) AS event_count
FROM fleet_events_raw
WHERE occurred_at > NOW() AT TIME ZONE 'UTC' - INTERVAL '1 HOUR'
GROUP BY vehicle_id, severity;""".rstrip()

